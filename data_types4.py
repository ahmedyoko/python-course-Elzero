# ..................
# type() to identify the data type
# All Data in Python Are Object
# ..........................
print(10) 
print(type(10))    #int => <class 'int'>
print(type(10.9))  #float => < class 'float'>
print(type("Hello Python")) #string => <class 'str'>
print(type([1,2,3,4,5])) # list => <class 'list'>
print(type((1,2,3,4,5))) # Tuple => <class 'tuple'>
print(type({"one": 1, "two": 2, "three":3}))  # Dictionary=> <class 'dict'>
print(type(2==3)) # Boolean => <class 'bool'>